package com.compiled_with_no_errors.exercises.mathematical_operations;

/**
 * This class implements mathematical operations for {@link Integer}.
 */
public class MathematicalOperations {

    /**
     * This methods computes multiplication of its parameters and returns the result.
     * @param x first parameter
     * @param y second parameter
     * @return result of x * y
     */
    public long multiplication(int x, int y){
        return (long) x * (long) y;
    }

    /**
     * This methods computes division of its parameters and returns the result.
     * @param x first parameter
     * @param y second parameter
     * @return result of x / y
     */
    public float division(int x, int y){
        return (float) x / (float) y;
    }
}
